 * @method static \Illuminate\Database\Eloquent\Builder<static>|Section whereUpdatedAt($value)
 * @mixin \Eloquent
 */
class Section extends Model
{
    protected $fillable = [
        'name',
    ];

    public function aspects():HasMany {
        return $this->hasMany(Aspect::class);
    }
}
